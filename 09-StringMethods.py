text = " python programming"

print(text.upper())   # converts all characters to uppercase
print(text.lower())  # converts all characters to lowercase
print(text.strip())  # removes leading and trailing whitespace
print(text.replace("python", "java"))  # replaces a substring with another
print(text.title())  # converts the first character of each word to uppercase
print(text.capitalize())  # converts the first character to uppercase and the rest to lowercase
print(text.split())  # splits the string into a list of substrings
print(text.find("programming"))  # finds the index of the first occurrence of the substring
print(text.count("o"))  # counts the number of occurrences of the substring
print(text.startswith("py"))  # checks if the string starts with the specified substring
print(text.endswith("ing"))  # checks if the string ends with the specified substring 